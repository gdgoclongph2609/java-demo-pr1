package tut1;

public class PrintMyAge {
    public static void main(String[] args) {
        int birthyear = 2002;
        int currentyear = 2021;
        int age = currentyear - birthyear;
        System.out.println("I was born in " + birthyear + ". " + "This year is " + currentyear + ".");
        System.out.println("Therefore, my age is " + age + ".");
    }

}
